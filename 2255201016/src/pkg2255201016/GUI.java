    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        Insert in = new Insert();
        String nama = inNama.getText();
        String jenis = "";
        if (inTart.isSelected()) {
            jenis = "Kue Tart";
        } else if (inKering.isSelected()) {
            jenis = "Kue Kering";
        } else if (inBasah.isSelected()) {
            jenis = "Kue Basah";
        }
        String custom = inCustom.getText();
        String alamat = inAlamat.getText();

        in.insert(nama, jenis, custom, alamat);
        table();


    }//GEN-LAST:event_jButton1ActionPerformed

    private void penjualanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_penjualanMouseClicked
        // TODO add your handling code here:
        int baris = penjualan.getSelectedRow();
        inNama.setText(penjualan.getValueAt(baris, 0).toString());
        String jenis = penjualan.getValueAt(baris, 1).toString();
        if (jenis.equals("Tart Cake")) {
            inTart.setSelected(true);
        } else if (jenis.equals("Jajanan Pasar")) {
            inBasah.setSelected(true);
        } else if (jenis.equals("Cookies")) {
            inKering.setSelected(true);
        }
        inCustom.setText(penjualan.getValueAt(baris, 2).toString());
        inAlamat.setText(penjualan.getValueAt(baris, 3).toString());
    }//GEN-LAST:event_penjualanMouseClicked

