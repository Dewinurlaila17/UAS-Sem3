    public void table() {
        Koneksi konek = new Koneksi();
        konek.koneksi();

        try {
            String sql = "select nama, jenis, custom, alamat from tblpenjualan";
            pst = (Statement) konek.con.prepareStatement(sql);
            rs = pst.executeQuery(sql);
            penjualan.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

